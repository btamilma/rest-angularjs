package com.btamilmani.dto;

public class TobacoReportDto {

	String year;
	String loc_abbr;
	String Loc_desc;
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getLoc_abbr() {
		return loc_abbr;
	}
	public void setLoc_abbr(String loc_abbr) {
		this.loc_abbr = loc_abbr;
	}
	public String getLoc_desc() {
		return Loc_desc;
	}
	public void setLoc_desc(String loc_desc) {
		Loc_desc = loc_desc;
	}
}
